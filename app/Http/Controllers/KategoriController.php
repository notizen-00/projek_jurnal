    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nama_kategori'=>'required|max:100|unique:kategori_produk'
    ]);

    $data = kategori::create($request->all());


    if($data){
        $response = array(
            'status'=>200,
            'message'=>'kategori '.$request->nama_kategori.' telah di tambah',
            'nama_kategori'=>$request->nama_kategori
        );
            echo json_encode($response);
    }else{
        $response = array(
            'status'=>201,
            'message'=>'kategori '.$request->nama_kategori.' gagal di tambah',
            'nama_unit'=>$request->nama_kategori
        );
            echo json_encode($response);
    }
    }

